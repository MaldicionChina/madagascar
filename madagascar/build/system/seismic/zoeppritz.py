sfzoeppritz.par('vp1',rsf.doc.rsfpar('float','','',''''''))
sfzoeppritz.par('vp2',rsf.doc.rsfpar('float','','',''''''))
sfzoeppritz.par('vs1',rsf.doc.rsfpar('float','','',''''''))
sfzoeppritz.par('vs2',rsf.doc.rsfpar('float','','',''''''))
